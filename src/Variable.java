public class Variable {

    public static void main(String[] args) {

        /*
         * une variable est un emplacement en mémoire qui peut contenir une valeur.Les
         * variables sont déclarées à l'aide du mot-clé var ou du mot-clé type suivi du
         * nom de la variable.
         */

        // Type de variable et ensuite le nom de la variable
        //Entier
        int nombre = 5;

        System.out.println("La valeur de nombre est: "+nombre);

        // long : entier sur 64 bits (de -9223372036854775808 // 9223372036854775807)
        long maVariableLong = 9223372036854775807L;

        //Réel
        float maVaraiableFloat = 3.14159f;

        //Double
        double mavariableDOuble = 3.14159;

        //Caractère
        char maVariableChar = 'a';

        //Booléen
        boolean maVariableBoolean = true;

        //Chaîne de caractères
        String maVariableString = "Java";



    }
}
